cidade_custo(Custo, Cidade) :-
    cidade(Cidade,
        _,
        caracteristicas(Custo, _, _),
        _,
        _).

cidade_popularidade(Popularidade, Cidade) :-
    cidade(Cidade,
        _,
        caracteristicas(_, _, Popularidade),
        _,
        _).

cidade_seguranca(Seguranca, Cidade) :-
    cidade(Cidade,
        _,
        caracteristicas(_,Seguranca, _),
        _,
        _).



cidade_clima_inicio(Clima, Cidade) :-
    cidade(Cidade,
        _,
        _,
        _,
        clima(Clima, _, _)).

cidade_clima_meio(Clima, Cidade) :-
    cidade(Cidade,
        _,
        _,
        _,
        clima(_, Clima, _)).

cidade_clima_fim(Clima, Cidade) :-
    cidade(Cidade,
        _,
        _,
        _,
        clima(_, _, Clima)).




cidade_lingua(Lingua, Cidade) :-
    cidade(Cidade,
        _,
        _,
        detalhes(Lingua,_,_),
        _).

cidade_pais(Pais, Cidade) :-
    cidade(Cidade,
        _,
        _,
        detalhes(_,Pais,_),
        _).

cidade_continente(Continente, Cidade) :-
    cidade(Cidade,
        _,
        _,
        detalhes(_,_,Continente),
        _).



cidade_nome(Cidade) :-
    cidade(Cidade,
        _,
        _,
        _,
        _).


atividades_cidade(Cidade, Atividades) :-
    cidade(Cidade,
        Atividades,
        _,_,_),
    write(Atividades).

detalhes_cidade(Cidade, Detalhes) :-
    cidade(Cidade,_,_,Detalhes,_),
    write(Detalhes).

main :-
    write('Digite as caracteristicas da cidade que deseja visitar: '), nl,
    write('Custo (baixo (padrao), medio, alto): '),
    read(Custo),
    write('Segurança (baixa, media, alta (padrao)): '),
    read(Seguranca),
    write('Popularidade (baixa, media, alta (padrao)): '),
    read(Popularidade),
    (
        Popularidade == baixa ->
        NovaPopularidade = popularidadebaixa
        ;
        Popularidade == media ->
        NovaPopularidade = popularidademedia
        ;
        NovaPopularidade = popularidadealta
    ),
    (
        Seguranca == baixa ->
        NovaSeguranca = segurancabaixa
        ;
        Seguranca == media ->
        NovaSeguranca = segurancamedia
        ;
        NovaSeguranca = segurancaalta
    ),
    (
        Custo == alto ->
        NovoCusto = altocusto
        ;
        Custo == medio ->
        NovoCusto = mediocusto
        ;
        NovoCusto = baixocusto

    ),
    write('Epoca do ano (inicio, meio, fim ou qualquer): '),
    read(Epoca),
    (    
        Epoca == inicio ->
        write('Clima no início do ano: (frio, ameno, quente, chuvoso ou qualquer)'),
        read(ClimaInicio)
        ;
        Epoca == meio ->
        write('Clima no meio do ano: (frio, ameno, quente, chuvoso ou qualquer)'),
        read(ClimaMeio)
        ;
        Epoca == fim ->
        write('Clima no fim do ano: (frio, ameno, quente, chuvoso ou qualquer)'),
        read(ClimaFim)
    ),
    nl, write('Cidades com as características desejadas:'), nl,
    cidade(Nome, _, caracteristicas(NovoCusto, NovaSeguranca, NovaPopularidade), _, clima(_, _, _)),
    write('Cidade: '), write(Nome), write('             '), atividades_cidade(Nome, Atividades), write('            '), detalhes_cidade(Nome, Detalhes), nl,
    fail.
